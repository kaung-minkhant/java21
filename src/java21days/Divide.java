package java21days;

public class Divide {
  public static void main(String[] args) {
    float a, b;
    a = 13;
    b = 3;
    float divider = a / b;
    float reminder = a % b;
    System.out.println(a + " divided by " + b);
    System.out.println("Result\tReminder");
    System.out.println(divider+ "\t" + reminder);
  }
}
